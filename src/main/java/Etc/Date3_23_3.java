package Etc;

public class Date3_23_3 {
    public static void main(String[] args) {
        int a, b = 5, c = 3;//Ctrl Alt L 은 줄맞춤

        a = b + c;
        System.out.printf("%d + %d = %d \n", b, c, a);

        a = b - c;
        System.out.printf("%d - %d = %d \n", b, c, a);

        a = b * c;
        System.out.printf("%d * %d = %d \n", b, c, a);

        a = b / c;
        System.out.printf("%d / %d = %d \n", b, c, a);

        a = b % c;
        System.out.printf("%d %% %d = %d \n", b, c, a);
    }
}
